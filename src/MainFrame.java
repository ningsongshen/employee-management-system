    private void jButtonSearchSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchSubmitActionPerformed
        // TODO add your handling code here:
        // Search for an employee and display it in the text are when found.


        if (jTextFieldSearchEmployeeNumber.getText() == null) {
            // If nothing is entered in the textbox
            jTextAreaSearchDisplayEmployee.setText("Please enter an employee number");
        } else {
            try {
                // Try getting the employee from the hashtable, and outputting it.
                EmployeeInfo empSearched = theHashTable.getEmployee(Integer.parseInt(jTextFieldSearchEmployeeNumber.getText()));

                // The below lines are to convert the integer key for sex and workLoc into human readable text. e.g. male is 1, female 2 etc.
                String sexText;
                String workLocText;
                switch (empSearched.getSex()) {
                    case 1:
                        sexText = "Male";
                        break;
                    case 2:
                        sexText = "Female";
                        break;
                    case 3:
                        sexText = "Other";
                        break;
                    case 4:
                        sexText = "Prefer not to say";
                        break;
                    default:
                        sexText = "Unknown";
                        break;
                }
                switch (empSearched.getWorkLoc()) {
                    case 1:
                        workLocText = "Mississauga";
                        break;
                    case 2:
                        workLocText = "Toronto";
                        break;
                    case 3:
                        workLocText = "Ottawa";
                        break;
                    case 4:
                        workLocText = "Halifax";
                        break;
                    case 5:
                        workLocText = "Miami";
                        break;
                    default:
                        workLocText = "Unknown";
                        break;
                }

                // If the found employee is a full time, display information with yearly salary
                if (empSearched instanceof FullTimeEmployee) {
                    FullTimeEmployee FTEmpSearched = (FullTimeEmployee) empSearched;
                    jTextAreaSearchDisplayEmployee.setText("Employee Type: Full Time"
                            + "\nName: " + FTEmpSearched.getFirstName() + " " + FTEmpSearched.getLastName()
                            + "\nSex: " + sexText
                            + "\nWork Location: " + workLocText
                            + "\nDeduction Rate: " + FTEmpSearched.getDeductRate()
                            + "\n\nYearly Salary: " + FTEmpSearched.getYearlySalary()
                            + "\nNet Income: " + FTEmpSearched.calcNetAnnualIncome());
                } else {
                    // Else, display the part time worker information without the yearly salary
                    PartTimeEmployee PTEmpSearched = (PartTimeEmployee) empSearched;
                    jTextAreaSearchDisplayEmployee.setText("Employee Type: Part Time"
                            + "\nName: " + PTEmpSearched.getFirstName() + " " + PTEmpSearched.getLastName()
                            + "\nSex: " + sexText
                            + "\nWork Location: " + workLocText
                            + "\nDeduction Rate: " + PTEmpSearched.getDeductRate()
                            + "\n\nHourly Wage: " + PTEmpSearched.getHourlyWage()
                            + "\nHours per Week: " + PTEmpSearched.getHoursPerWeek()
                            + "\nWeeks per Year: " + PTEmpSearched.getWeeksPerYear()
                            + "\nNet Income: " + PTEmpSearched.calcNetAnnualIncome());
                }
            } catch (ArrayIndexOutOfBoundsException | NumberFormatException e) {
                // If the employee is not found, the output is below. Either nothing is found or error occured possible (arrayindexoutofbounds)
                jTextAreaSearchDisplayEmployee.setText("Employee not found");
            }
        }
    }//GEN-LAST:event_jButtonSearchSubmitActionPerformed

    private void jMenuItemFileSaveAsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemFileSaveAsActionPerformed

        // Open a file chooser dialog
        //jFileChooser.setVisible(true);
        
        javax.swing.JFileChooser fileChooser = new javax.swing.JFileChooser();
        fileChooser.setDialogTitle("Specify a file to save");
        // Set the file chooser to save
        int userSelection = fileChooser.showSaveDialog(jFileChooserSaveFile);

        // Create a textarea to prepare the text to be written to the file. JTextcomponents have the ability to write to a file, and a text area is a JTextComponent
        javax.swing.JTextArea hashTableData = new javax.swing.JTextArea();

        // Converting the exported hashTableArray into strings that can be printed into a txt file
        String[][] hashTableArray = theHashTable.exportHashTable();
        // Run through the entire has table in the same way as display contents and use # as separator
        // NOTE: unused indices are filled in with null
        for (int i = 0; i < theHashTable.getEmployeeCount(); i++) {
            for (int j = 0; j < 11; j++) {
                hashTableData.append(hashTableArray[i][j] + "/");
            }
            hashTableData.append("\n");
        }

        if (userSelection == javax.swing.JFileChooser.APPROVE_OPTION) {
            // If the user says ok to saving the file
            java.io.File fileToSave = fileChooser.getSelectedFile();
            if (fileToSave == null) {
                return;
            }
            // Append .txt to the file name

            // Automatically save the file as a txt file
            fileToSave = new java.io.File(fileToSave.getParentFile(), fileToSave.getName() + "-[DO NOT EDIT].txt");


            try {
                // Try saving the file
                hashTableData.write(new java.io.OutputStreamWriter(new java.io.FileOutputStream(fileToSave),
                        "utf-8"));
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jMenuItemFileSaveAsActionPerformed

